    private void tb_dataKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_dataKeyReleased
        DefaultTableModel model = (DefaultTableModel) tb_data.getModel();
        int myindex = tb_data.getSelectedRow();
        txt_nama.setText(model.getValueAt(myindex, 0).toString());
        txt_nik.setText(model.getValueAt(myindex, 2).toString());
        txt_tempatlahir.setText(model.getValueAt(myindex, 3).toString());
        tgl_lahir.setDate(new Date(Long.parseLong(String.valueOf(timestamp_tgl_lahir.get(myindex) + "000"))));
        txt_alamat.setText(model.getValueAt(myindex, 6).toString());

        switch (model.getValueAt(myindex, 5).toString()) {
            case "WNI":
                cb_kewarnegaraan.setSelectedIndex(0);
                break;
            case "WNA":
                cb_kewarnegaraan.setSelectedIndex(1);
                break;
        }
        for (int i = 0; i < jabatan.size(); i++) {
            if (model.getValueAt(myindex, 7).toString().equals(jabatan.get(i))) {
                cb_jabatan.setSelectedIndex(i);
                break;
            }
        }
        switch (model.getValueAt(myindex, 8).toString()) {
            case "LAKI-LAKI":
                buttonGroup1.setSelected(rd_laki.getModel(), rootPaneCheckingEnabled);
                break;
            case "PEREMPUAN":
                buttonGroup1.setSelected(rd_perempuan.getModel(), rootPaneCheckingEnabled);
                break;
        }
        switch (model.getValueAt(myindex, 8).toString()) {
            case "LAKI-LAKI":
                buttonGroup1.setSelected(rd_laki.getModel(), rootPaneCheckingEnabled);
                break;
            case "PEREMPUAN":
                buttonGroup1.setSelected(rd_perempuan.getModel(), rootPaneCheckingEnabled);
                break;
        }
        switch (model.getValueAt(myindex, 9).toString()) {
            case "ISLAM":
                cb_agama.setSelectedIndex(0);
                break;
            case "KRISTEN":
                cb_agama.setSelectedIndex(1);
                break;
            case "HINDU":
                cb_agama.setSelectedIndex(2);
                break;
            case "BUDHA":
                cb_agama.setSelectedIndex(3);
                break;
            case "KONGHUCHU":
                cb_agama.setSelectedIndex(4);
                break;
        }
        txt_gaji.setText(model.getValueAt(myindex, 10).toString());
        cb_status.setEnabled(true);
        btn_tambah.setEnabled(false);

        switch (model.getValueAt(myindex, 11).toString()) {
            case "AKTIF":
                cb_kewarnegaraan.setSelectedIndex(0);
                break;
            case "NONAKTIF":
                cb_kewarnegaraan.setSelectedIndex(1);
                break;
        }

        setFoto(image.get(myindex));
        refreshFoto();
    }//GEN-LAST:event_tb_dataKeyReleased

