    private void btn_tambahjabatanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahjabatanActionPerformed
        // TODO add your handling code here:
        String tambahJabatan;
        try {
            tambahJabatan = JOptionPane.showInputDialog("masukkan jabatan baru");
            ps = conn.prepareStatement("insert into tb_config values (NULL,?)");
            ps.setString(1, tambahJabatan);
            ps.executeUpdate();
            jabatan.clear();
            cb_jabatan.removeAllItems();
            cs = conn.createStatement();
            rs = cs.executeQuery("select * from tb_config");
            while (rs.next()) {
                jabatan.add(rs.getString("jabatan").toUpperCase());
                cb_jabatan.addItem(rs.getString("jabatan").toUpperCase());
            }
            JOptionPane.showMessageDialog(null, "JABATAN BERHASIL DITAMBAHKAN", "BERHASIL", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "TERDAPAT KESALAHAN DATA", "KESALAHAN", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_tambahjabatanActionPerformed

